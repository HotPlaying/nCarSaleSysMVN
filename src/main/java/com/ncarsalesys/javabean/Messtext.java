package com.ncarsalesys.javabean;

public class Messtext {
    private String cid;
    private String sid;
    private String cmt;
    private String lem;

    public String getCid() {
        return cid;
    }

    public void setCid(String cid) {
        this.cid = cid;
    }

    public String getSid() {
        return sid;
    }

    public void setSid(String sid) {
        this.sid = sid;
    }

    public String getCmt() {
        return cmt;
    }

    public void setCmt(String cmt) {
        this.cmt = cmt;
    }

    public String getLem() {
        return lem;
    }

    public void setLem(String lem) {
        this.lem = lem;
    }

}
